  // Get all the list items and add click event listeners
  document.querySelectorAll('.region-item').forEach(item => {
    item.addEventListener('click', function() {
        // Get the data-region attribute to identify the clicked region
        let region = this.getAttribute('data-region');

        // Remove 'highlight' class from all regions first
        document.querySelectorAll('.map-area').forEach(area => {
            area.classList.remove('highlight');
        });

        // Add the 'highlight' class to the clicked region
        document.getElementById(region).classList.add('highlight');
    });
});
